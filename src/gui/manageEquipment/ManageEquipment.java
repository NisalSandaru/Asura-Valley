    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String eqname = jTextField1.getText();
        String qty = jTextField2.getText();
        String dsc = jTextArea1.getText();

        if (eqname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "plz Enter Equipment Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (dsc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "plz Enter description", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                int qtyValue = Integer.parseInt(qty);  
                if (qtyValue <= 0) {
                    JOptionPane.showMessageDialog(this, "Please enter a quantity greater than 0", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    try {
                        ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `equipment` WHERE `name` = '" + eqname + "'");

                        if (resultSet.next()) {
                            JOptionPane.showMessageDialog(this, "Equipment already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                        } else {
                            MySQL2.executeIUD("INSERT INTO `equipment` (`name`, `qty`, `description`, `employee_email`) "
                                    + "VALUES ('" + eqname + "', '" + qtyValue + "', '" + dsc + "', '" + email + "' ) ");

                            loadEquipment();
                            reset();
                        }

                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Please enter a valid integer for qty", "Warning", JOptionPane.WARNING_MESSAGE);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

