    public void loadPenOrders(String mobile) {
        try {

            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `bookings` INNER JOIN `customer` ON `customer`.`mobile` = `bookings`.`customer_mobile` WHERE `b_status_id` = '2' AND `customer`.`mobile` LIKE '" + mobile + "%' ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("customer.email"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("ar_date"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

