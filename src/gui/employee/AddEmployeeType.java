    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            
            String name = jTextField1.getText();

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Job role", "Warning", JOptionPane.WARNING_MESSAGE);
            } else{
                
                try {

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `employee_type` WHERE `name` = '" + name + "' ");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Job role already added", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL2.executeIUD("INSERT INTO `employee_type` (`name`) VALUES ('" + name + "') ");

                    loadJobs();
                    reset();
                    me.setJobRole();
                    
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
                
            }
 
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int row = jTable1.getSelectedRow();

        if (row == -1) {
                JOptionPane.showMessageDialog(this, "Please Select Row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String name = jTextField1.getText();

            String selectedId = String.valueOf(jTable1.getValueAt(row, 0));

            String selectedName = String.valueOf(jTable1.getValueAt(row, 1));

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);

            }else if (selectedName.equals(name)) {
                JOptionPane.showMessageDialog(this, "Please Chanege job role to update", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                try {

                    ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `employee_type` WHERE `name` = '" + name + "' ");

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "Job role already Added", "Warning", JOptionPane.WARNING_MESSAGE);

                    } else {

                        MySQL2.executeIUD("UPDATE `employee_type` SET `name` = '" + name + "' WHERE `id` = '" + selectedId + "' ");

                        loadJobs();
                        reset();
                        me.setJobRole();

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

